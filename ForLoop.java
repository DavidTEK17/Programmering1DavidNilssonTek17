package loops;

public class ForLoop {

	public static void main(String[] args) {
		
		int number = 0;
		
		System.out.println(number);
		number = number + 1;
		System.out.println(number);
		number += number;
		System.out.println(number);
		number++;
		System.out.println(number);
		
		
		for(int i = 0; i <= 10; i++) {
			System.out.println(i);

	}

}
	
}
